# 3. Узнайте у пользователя число n. Найдите сумму чисел n + nn + nnn. Например, пользователь ввёл число 3.
# Считаем 3 + 33 + 333 = 369.

var_1 = input('Введите число n: ')
sum_1 = int (var_1)
sum_2 = int (var_1 + var_1)
sum_3 = int (var_1 + var_1 + var_1)

print ("Сумма чисел n + nn + nnn: ", sum_1 + sum_2 + sum_3)



